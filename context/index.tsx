// context/index.tsx
"use client";

import { wagmiAdapter, projectId } from "../config";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { createAppKit } from "@reown/appkit/react";
import {
  mainnet,
  arbitrum,
  avalanche,
  base,
  optimism,
  polygon,
} from "@reown/appkit/networks";
import React, { type ReactNode } from "react";
import { cookieToInitialState, WagmiProvider, type Config } from "wagmi";
import { UserProvider } from "../src/context/UserContext";

// Set up queryClient
const queryClient = new QueryClient();

if (!projectId) {
  throw new Error("Project ID is not defined");
}

// Set up metadata
const metadata = {
  name: "WarpSell",
  description: "A decentralized marketplace for web3 stablecoins based on TCG cards.",
  url: "https://WrapSell.com/appkit", // origin must match your domain & subdomain
  icons: ["/Simbol.png"],
};

// Create the modal
const modal = createAppKit({
  adapters: [wagmiAdapter],
  projectId,
  networks: [mainnet, arbitrum, avalanche, base, optimism, polygon],
  defaultNetwork: mainnet,
  metadata: metadata,
  features: {
    analytics: true, // Optional - defaults to your Cloud configuration
    socials: ['google', 'apple', 'github', 'discord'], // Optional - defaults to your Cloud configuration
    emailShowWallets: true, // Optional - defaults to your Cloud configuration
  },
  themeMode: "light",
  // Configuraciones para mejorar la persistencia
  enableEIP6963: true,
  enableCoinbase: true,
  // Optional - defaults to your Cloud configuration
});

function ContextProvider({
  children,
  cookies,
}: {
  children: ReactNode;
  cookies: string | null;
}) {
  const initialState = cookieToInitialState(
    wagmiAdapter.wagmiConfig as Config,
    cookies
  );

  return (
    <WagmiProvider
      config={wagmiAdapter.wagmiConfig as Config}
      initialState={initialState}
    >
      <QueryClientProvider client={queryClient}>
        <UserProvider>
          {children}
        </UserProvider>
      </QueryClientProvider>
    </WagmiProvider>
  );
}

export default ContextProvider;
